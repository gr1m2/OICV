#include <bits/stdc++.h>

using namespace std; //Para poder usar operadores como "cin" o "cout"

int number; //Creación de la variable en la cual se almacena el número

int main(){ //Variable donde estara el codigo ejecutado
    cout << "Number: "; //Palabra que aparece al iniciar el programa
    cin >> number; //Para poder escribir usamos "Cin", number es la variable del principio donde se va a almacenar el número
    cout << number+1; //Aparece en pantalla el sucesor del numero escrito (Al número le sumamos uno y ya tenemos el sucesor)
}